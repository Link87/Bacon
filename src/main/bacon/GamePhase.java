package bacon;

/**
 * An enum for the phases a game can be in.
 */
public enum GamePhase {
    /**
     * The variant representing the main playing phase.
     */
    PHASE_ONE,

    /**
     * The variant representing the bombing phase.
     */
    PHASE_TWO,

    /**
     * The variant representing a game that has ended.
     */
    ENDED
}
